/**
 * Pre-flight Check - Task Quality Validation
 *
 * Validates task quality before dispatch to prevent wasting executor resources.
 *
 * @module pre-flight-check
 */

import { raise } from './alerting.js';
import { loadActiveConstraints, evaluateConstraints } from './insight-constraints.js';

// 24h 内累计 pre-flight cancel >= 阈值 → 从 P2 升级到 P0（飞书立即推送）
export const PRE_FLIGHT_ALERT_THRESHOLD = 3;

/**
 * Perform pre-flight check on a task
 *
 * @param {Object} task - Task object from database
 * @param {string} task.id - Task ID
 * @param {string} task.title - Task title
 * @param {string} task.description - Task description (PRD content)
 * @param {string} task.priority - Priority (P0/P1/P2)
 * @param {Object} [opts] - Options
 * @param {Object} [opts.dbPool] - Optional pg pool override (test injection)
 * @returns {Promise<Object>} Check result
 * @returns {boolean} .passed - Whether check passed
 * @returns {Array<string>} .issues - List of issues found
 * @returns {Array<string>} .suggestions - Suggestions for fixing issues
 */
export async function preFlightCheck(task, opts = {}) {
  const issues = [];
  const suggestions = [];

  // System-generated tasks do not need PRDs — skip description/content checks
  const SYSTEM_TASK_TYPES = ['dept_heartbeat', 'codex_qa', 'initiative_verify', 'initiative_plan', 'code_review', 'prd_review', 'spec_review', 'code_review_gate', 'initiative_review',
    'crystallize', 'crystallize_scope', 'crystallize_forge', 'crystallize_verify', 'crystallize_register',
    // Content pipeline sub-tasks and publish tasks — auto-generated by orchestrator, no PRD needed
    'content-pipeline', 'content-research', 'content-copywriting', 'content-copy-review',
    'content-generate', 'content-image-review', 'content-export', 'content_publish',
    // Harness pipeline tasks — auto-generated by harness pipeline, no separate PRD needed
    'harness_planner', 'harness_contract_propose', 'harness_contract_review',
    'harness_generate', 'harness_fix', 'harness_evaluate', 'harness_report',
    // Harness v2 (M1) — auto-generated by harness v2 pipeline (Initiative / Task / Final E2E)
    'harness_initiative', 'harness_task', 'harness_final_e2e'];
  const isSystemTask = SYSTEM_TASK_TYPES.includes(task.task_type);

  // Check 1: Title validation
  if (!task.title || task.title.trim().length === 0) {
    issues.push('Task title is empty');
    suggestions.push('Provide a descriptive title');
  } else if (task.title.trim().length < 5) {
    issues.push('Task title too short (< 5 characters)');
    suggestions.push('Use a more descriptive title (minimum 5 characters)');
  }

  // Check 2: Description validation (PRD content)
  // System tasks (dept_heartbeat, codex_qa, etc.) are auto-generated — no PRD required
  // Use description / prd_content / payload.prd_summary as fallback —
  // autonomous-created tasks often place the PRD in payload.prd_summary
  // instead of the top-level description column.
  const descContent = task.description || task.prd_content || task.payload?.prd_summary;
  if (!isSystemTask) {
    if (!descContent || descContent.trim().length === 0) {
      issues.push('Task description is empty');
      suggestions.push('Provide a PRD with clear requirements');
    } else if (descContent.trim().length < 20) {
      issues.push('Task description too short (< 20 characters)');
      suggestions.push('Provide more detailed requirements in the PRD');
    }
  }

  // Check 3: Priority validation (with normalization).
  // Map common non-standard priority labels (urgent/high/normal/medium/low/critical)
  // onto P0/P1/P2 before validating, so upstream creators using semantic
  // names aren't rejected outright.
  const PRIORITY_NORMALIZE_MAP = {
    urgent: 'P0',
    critical: 'P0',
    high: 'P1',
    normal: 'P2',
    medium: 'P2',
    low: 'P2',
  };
  if (task.priority && typeof task.priority === 'string') {
    const mapped = PRIORITY_NORMALIZE_MAP[task.priority.toLowerCase()];
    if (mapped) task.priority = mapped;
  }

  const validPriorities = ['P0', 'P1', 'P2'];
  if (!task.priority || !validPriorities.includes(task.priority)) {
    issues.push(`Invalid priority: ${task.priority || 'undefined'}`);
    suggestions.push(`Set priority to one of: ${validPriorities.join(', ')}`);
  }

  // Check 4: Skill validation (if specified)
  if (task.skill) {
    const validSkills = ['/dev', '/qa', '/audit', '/review'];
    if (!validSkills.includes(task.skill)) {
      issues.push(`Unknown skill: ${task.skill}`);
      suggestions.push(`Use a valid skill: ${validSkills.join(', ')}`);
    }
  }

  // Check 5: Description content quality (basic heuristics)
  if (descContent) {
    // Strip markdown code blocks and inline code before placeholder detection,
    // so that examples like `- Task ID: xxx` or ```sql ... xxx``` don't false-positive.
    const stripped = descContent
      .replace(/```[\s\S]*?```/g, '')   // remove fenced code blocks
      .replace(/`[^`]*`/g, '');          // remove inline code spans
    const desc = stripped.toLowerCase();

    // Check for placeholder text using word boundaries to avoid false positives.
    // e.g. "TODO 记录" or "fixme this" in prose vs a bare "todo" / "tbd" as description.
    // `xxx` is only flagged when surrounded by non-CJK characters (avoids Chinese prose context).
    const hasWordPlaceholder = /\b(todo|tbd|fixme)\b/.test(desc);
    // `xxx` flagged only as a standalone token (word boundary, not inside Chinese text)
    // eslint-disable-next-line no-control-regex
    const hasXxx = /(?<![^\u0000-\u007F])xxx(?![^\u0000-\u007F])/.test(stripped.toLowerCase());
    const hasPlaceholder = hasWordPlaceholder || hasXxx;

    if (hasPlaceholder) {
      issues.push('Description contains placeholder text');
      suggestions.push('Replace placeholder text with actual requirements');
    }

    // Check for minimal effort descriptions
    if (desc.trim() === 'test' || desc.trim() === 'fix' || desc.trim() === 'update') {
      issues.push('Description is too generic');
      suggestions.push('Provide specific details about what needs to be done');
    }
  }

  // Check 6: Insight-derived dispatch constraints —
  // 把 cortex_insight 通过 learnings.dispatch_constraint 转化为硬规则，
  // 避免"洞察 ≠ 约束"导致系统重复踩坑（learning_id d4405cc0）。
  // 加载/求值任一失败都不应反向阻塞 dispatch，pre-flight 只追加 issues/suggestions。
  try {
    const constraints = await loadActiveConstraints(opts.dbPool);
    const insightResult = evaluateConstraints(task, constraints);
    if (insightResult.issues.length > 0) issues.push(...insightResult.issues);
    if (insightResult.suggestions.length > 0) suggestions.push(...insightResult.suggestions);
  } catch (err) {
    console.warn('[pre-flight] insight-constraint evaluation failed (non-fatal):', err.message);
  }

  // Check 7: Repeated-failure guard — 阻断无限重试死循环
  // 任务已被 quarantine（repeated_failure）但仍进入 dispatch 队列时，直接拒绝。
  // 根因：thalamus L0 在 retry_count 未正确递增时会无限 retry_task，
  // 使 repeated_failure 任务反复派发但每次必然失败（外部依赖缺失等结构性问题）。
  const quarantineReason = task.payload?.quarantine_reason || task.metadata?.quarantine_reason;
  const failureCount = task.payload?.failure_count || task.payload?.watchdog_retry_count || 0;
  if (quarantineReason === 'repeated_failure' || failureCount >= 5) {
    issues.push(`任务已标记为 repeated_failure（failure_count=${failureCount}），拒绝派发`);
    suggestions.push('请人工排查根因（外部依赖缺失/执行路径错误），修复后手动 reset 任务状态');
  }

  const passed = issues.length === 0;

  return {
    passed,
    issues,
    suggestions
  };
}

/**
 * Get pre-flight check statistics from working_memory
 *
 * @param {Object} pool - Database pool
 * @returns {Promise<Object>} Statistics
 */
export async function getPreFlightStats(pool) {
  const result = await pool.query(`
    SELECT
      COUNT(*) FILTER (WHERE metadata->>'pre_flight_failed' = 'true') as failed_count,
      COUNT(*) FILTER (WHERE metadata->>'pre_flight_failed' IS NULL) as passed_count,
      jsonb_agg(metadata->'pre_flight_issues') FILTER (WHERE metadata->>'pre_flight_failed' = 'true') as all_issues
    FROM tasks
    WHERE created_at > NOW() - INTERVAL '7 days'
  `);

  const row = result.rows[0];
  const failedCount = parseInt(row.failed_count) || 0;
  const passedCount = parseInt(row.passed_count) || 0;
  const totalChecked = failedCount + passedCount;

  const passRate = totalChecked > 0 ? (passedCount / totalChecked * 100).toFixed(2) : 0;

  // Count issue types
  const issueDistribution = {};
  if (row.all_issues) {
    const allIssues = row.all_issues.flat().filter(Boolean);
    allIssues.forEach(issue => {
      issueDistribution[issue] = (issueDistribution[issue] || 0) + 1;
    });
  }

  return {
    totalChecked,
    passed: passedCount,
    failed: failedCount,
    passRate: `${passRate}%`,
    issueDistribution
  };
}

/**
 * 在 tick.js 对任务打上 pre_flight_failed 标记之后调用。
 * 通过 alerting.raise() 推送飞书告警，防止 pre-flight cancel 静默堆积。
 *
 * - 单次失败 → raise('P2', 'pre_flight_cancel', ...)（进 24h 汇总）
 * - 24h 内累计 fail_count >= PRE_FLIGHT_ALERT_THRESHOLD (默认 3)
 *   → raise('P0', 'pre_flight_burst', ...)（飞书立即推送）
 *
 * 注意：此函数必须在 tick.js 写入 metadata.pre_flight_failed=true 之后调用，
 * 这样 SELECT COUNT 才能把当前这次失败也算进去。
 *
 * @param {Object} pool - pg Pool 实例
 * @param {Object} task - 被 cancel 的任务（含 id/title）
 * @param {Object} checkResult - preFlightCheck 返回值（含 issues 数组）
 * @returns {Promise<void>} 不抛异常（告警失败不应影响 dispatch 主流程）
 */
export async function alertOnPreFlightFail(pool, task, checkResult) {
  try {
    const taskTitle = task.title || '(untitled)';
    const issuesArr = Array.isArray(checkResult?.issues) ? checkResult.issues : [];
    const issues = issuesArr.length > 0 ? issuesArr.join('; ') : '(no specific issues)';
    const basicMsg = `Pre-flight 拒绝任务 "${taskTitle}" (${task.id}): ${issues}`;

    // 查 24h 内累计 cancel 数量（此时本次 task 已写入 metadata，会被统计进去）
    const countResult = await pool.query(
      `SELECT COUNT(*)::int AS c FROM tasks
       WHERE metadata->>'pre_flight_failed' = 'true'
         AND (metadata->>'failed_at')::timestamptz > NOW() - INTERVAL '24 hours'`
    );
    const recentCount = countResult.rows?.[0]?.c || 0;

    if (recentCount >= PRE_FLIGHT_ALERT_THRESHOLD) {
      await raise('P0', 'pre_flight_burst',
        `【URGENT】24h 内 ${recentCount} 个任务被 pre-flight 拒绝！最新：${basicMsg}`
      );
    } else {
      await raise('P2', 'pre_flight_cancel', basicMsg);
    }
  } catch (err) {
    // 告警失败不能反向影响 dispatch 主流程
    console.warn('[pre-flight-alerting] alertOnPreFlightFail failed:', err?.message || err);
  }
}
